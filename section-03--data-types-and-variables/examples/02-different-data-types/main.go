package main

import "fmt"

func main() {
	// string
	var message string = "Go is awesome"
	fmt.Println(message)

	// whole number
	var number int = 14
	fmt.Println(number)

	// real number
	var realNum float32 = 1.22
	fmt.Println(realNum)

	// characters
	var ch rune = 'S'
	fmt.Println(ch)

	//boolean
	var isTrue = true
	fmt.Println(isTrue)
}
